
public class Van 
{
	 Van()
	{
		System.out.println("I am Zero parameterised constructor");
	}
	
	Van(int a)
	{
		System.out.println("I am int parameterised constructor");
	}
	
	Van(double b)
	{
		System.out.println("I am double parameterised constructor");
	}
	
	
	Van(int a, int b)
	{
		System.out.println("I am int,int parameterised constructor");
	}
	
}
