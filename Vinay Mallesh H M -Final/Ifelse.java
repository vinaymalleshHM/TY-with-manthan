class Ifelse
{
	public static void main(String ar[])
	{
		int per=65;

		if(per>=70)
		{
			System.out.println("fcd");
		}
		else if(per>=60 )
		{
			System.out.println("fc");

		}

		else if(per>=50  )
		{
			System.out.println("sc");

		}
		
		else if(per>=40)
		{
			System.out.println("tc");

		}

		else
		{
			System.out.println("fail");

		}
		
		
	}
}